package graphTheory.utils.probabilities;

/**
 * Probability law returning an integer.
 * @author mouton
 *
 */
public abstract class DiscreteProbabilityLaw {
	
	/**
	 * Simulate this discrete probability law and return an integer value
	 * depending on the law.
	 * @return
	 */
	public abstract int simulate();
}

