package graphTheory.utils.probabilities;

/**
 * Probability law returning a double
 * @author mouton
 *
 */
public abstract class ProbabilityLaw {
	
	/**
	 * Simulate this probability law and return a double value
	 * depending on the law.
	 * @return
	 */
	public abstract double simulate();
}

